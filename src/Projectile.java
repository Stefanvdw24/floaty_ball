public class Projectile {
    double rx, ry, vx, vy;
    final double radius;
    
    public Projectile(double rx,double ry,double vx,double vy,double radius) 
    {
       this.rx = rx;
       this.ry = ry;
       this.vx = vx;
       this.vy = vy;
       this.radius = radius;
    }

}